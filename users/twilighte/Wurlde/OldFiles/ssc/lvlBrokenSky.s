DisplayTiltFrame
	lda #<$A758+47*40
	sta screen
	lda #>$A758+47*40
	sta screen+1
	ldx TiltFrameIndex
	lda TiltAnimAddressLo,x
	sta source
	lda TiltAnimAddressHi,x
	sta source+1
	ldx #33
.(
loop2	ldy #10
loop1	lda (source),y
	sta (screen),y
	dey
	bpl loop1

	lda source
	clc
	adc #11
	sta source
	lda source+1
	adc #00
	sta source+1

	lda screen
	adc #40
	sta screen
	lda screen+1
	adc #00
	sta screen+1

	dex
	bne loop2
.)
	rts

ManageCable
	lda #6
	sec
	sbc TiltFrameIndex
	sta temp02
	;0-5

	;Delete Previous
	ldy temp02
.(
	beq skip1
	dey
	jsr DeleteCable
	ldy temp02
skip1	jsr PlotCable
.)
	rts





;Y==CableNumber 0-5
PlotCable
	ldx CablePointCount,y
	dex
	lda CableXTableLo,y
.(
	sta vector1+1
	lda CableXTableHi,y
	sta vector1+2
	lda CableYTableLo,y
	sta vector2+1
	lda CableYTableHi,y
	sta vector2+2

loop1	;Fetch X
vector1	ldy $dead,x
	;Get bit
	lda bsBitpos,y
	sta temp01
	;Get XLOC
	lda bsXLOC,y
	;Get Y
vector2	ldy $dead,x
	;Add Row address
	clc
	adc bsSYLocl,y
	sta screen
	lda bsSYLoch,y
	adc #00
	sta screen+1
	;Bit OR Screen
	ldy #00
	lda temp01
	ora (screen),y
	sta (screen),y
	dex
	bpl loop1
.)
	rts

;Y==CableNumber 0-5
DeleteCable
	ldx CablePointCount,y
	dex
	lda CableXTableLo,y
.(
	sta vector1+1
	lda CableXTableHi,y
	sta vector1+2
	lda CableYTableLo,y
	sta vector2+1
	lda CableYTableHi,y
	sta vector2+2

loop1	;Fetch X
vector1	ldy $dead,x
	;Get bit
	lda bsBitpos,y
	sta temp01
	;Get XLOC
	lda bsXLOC,y
	;Get Y
vector2	ldy $dead,x
	;Add Row address
	clc
	adc bsSYLocl,y
	sta screen
	lda bsSYLoch,y
	adc #00
	sta screen+1
	;NOT Bit AND Screen
	ldy #00
	lda temp01
	eor #63
	and (screen),y
	sta (screen),y
	dex
	bpl loop1
.)
	rts

