LevelRun
	;Delay animation
	lda TiltDelay
	clc
	adc #128
	sta TiltDelay
.(
	bcc skip1
	ldx TiltFrameIndex
	bmi skip1

	jsr ManageCable
	jsr DisplayTiltFrame
	dec TiltFrameIndex
	bpl skip1
	;Once complete, rescan BGB
	jsr CaptureBGB
	;and reset LevelSettings flag
	lda LevelSettings
	and #%11111110
	sta LevelSettings
skip1	rts
.)

