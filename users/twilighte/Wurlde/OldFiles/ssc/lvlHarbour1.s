	sta vector1+1
	lda FlagWaveFrameAddressHi,x
	sta vector1+2
	ldx #20
vector1	lda $dead,x
	ldy FlagWaveOffset,x
	sta $A76D,y
	dex
	bpl vector1
	dec FlagFrame
	bpl skip1
	lda #03
	sta FlagFrame
