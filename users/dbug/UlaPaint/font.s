_TextY	.byt 0



; =========================================
; void TextPos(unsigned char x,unsigned char y)
; =========================================
;	   Set the display position of text
; =========================================
_TextPos
.(
    ldy #0			; Grab first 8-bit parameter
    lda (sp),y		
    sta _TextX

	ldy #2
    lda (sp),y		; Grab second 8-bit parameter
    sta _TextY
	rts
.)


; =========================================
;	   Store screen adress in <tmp1>
; =========================================
_TextSetScreenPointer
.(
	;
	; Compute pointer on screen
	;
	pha
	txa
	pha

	clc
	ldx _TextY
	lda #<$bb80
	adc _HiresAddrLow,x
	sta tmp1+0
	lda #>$bb80
	adc _HiresAddrHigh,x
	sta tmp1+1

	pla
	tax
	pla
	rts
.)



; =========================================
; void TextPrint(char *ptr_message)
; =========================================
;	   Display a string of characters
; =========================================
_TextPrint
.(
	jsr _TextSetScreenPointer

    ldy #1
    lda (sp),y
    sta loop_read+2
    dey
    lda (sp),y
    sta loop_read+1

	ldx #0
begin_line
	ldy _TextX
loop_read
	lda $1234,x
	beq end_loop	; End of string
	inx 

	cmp #10			; Carriage return
	bne draw_char

	; Reposition screen pointeur
	inc _TextY
	jsr _TextSetScreenPointer
	jmp begin_line

draw_char
	sta (tmp1),y
	iny 
	jmp loop_read

end_loop
	sty _TextX
	rts
.)




